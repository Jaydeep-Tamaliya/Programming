package com.jsp.NumberSystemExample;
//write a program to check the given number is even or not.
public class IsEvenOrNot {
	public static void main(String[] args) {
		int n=11;
		
		if(n%2==0)
			System.out.println(n+" is even numbers");
		
		else
			System.out.println(n+" is odd numbers");
		
	}
}
