package com.jsp.NumberSystemExample;
//write a program to check the given number is even or not without using %.
public class IsEvenOrNotWithoutModulous {
	public static void main(String[] args) {
		int n=0;
		
		if(n/2*2==n)
			System.out.println(n+" is even numbers");
		
		else
			System.out.println(n+" is odd numbers");
	}
}
